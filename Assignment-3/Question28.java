/**
 * 28. Initialize one String type of array and print the elements using for each
 * loop.
 * 
 * 
 */
public class Question28 {
    public static void main(String[] args) {

        String arr[] = {"AMAN", "KAYARE", "CDAC", "KHARGHAR", "MUMBAI" };

        /*
         * String arr[] = new String[5]; arr[0] = "AMAN"; arr[1] = "KAYARE"; arr[2] =
         * "CDAC"; arr[3] = "KHARGHAR"; arr[4] = "MUMBAI";
         */
        for (String s : arr) {
            System.out.println(s);
        }
    }
}
